# frozen_string_literal: true

module DiversBot
  module Services
    module Messages
      module_function

      def welcome
        <<~TEXT
          🦪 Добро пожаловать в бот сбора данных об устрицах!

          Этот бот предназначен для дайверов, которые обнаружили устриц во время погружений. Собранные данные и фотографии будут переданы учёным для исследования.

          📋 *Что потребуется указать:*
          • Дата наблюдения
          • Место обнаружения (карта, координаты или описание)
          • Тип встречи (единичная или множественная)
          • Глубина наблюдения
          • Фото плотности поселения
          • Тип субстрата (с возможным фото)
          • Дополнительная информация (необязательно)
          • Дополнительные фото с описанием (необязательно)

          📸 *Примеры фотографий* (будут добавлены позже):
          _Здесь появятся образцы фото плотности поселения и субстрата._

          Нажмите «Начать отчёт», чтобы приступить к заполнению.
        TEXT
      end

      def ask_date
        <<~TEXT
          📅 *Дата наблюдения*

          Укажите дату, когда были обнаружены устрицы.
          Формат: `ДД.ММ.ГГГГ` (например, 15.06.2026)
        TEXT
      end

      def ask_location_choice
        <<~TEXT
          📍 *Место обнаружения*

          Выберите способ указания места:
          • *На карте* — выберите точку на интерактивной карте
          • *Координаты* — введите широту и долготу вручную
          • *Описание* — укажите название акватории и ориентиры словами
        TEXT
      end

      def ask_map_location
        <<~TEXT
          🗺 *Выбор точки на карте*

          Нажмите кнопку «Выбрать на карте» для интерактивного выбора точки, либо отправьте текущую геопозицию через кнопку «Отправить геопозицию».
        TEXT
      end

      def ask_coordinates
        <<~TEXT
          🌐 *Координаты*

          Введите широту и долготу через запятую или пробел.
          Пример: `55.7558, 37.6173` или `55.7558 37.6173`

          После ввода бот покажет точку на карте для проверки.
        TEXT
      end

      def ask_text_location
        <<~TEXT
          📝 *Описание места*

          Опишите место обнаружения словами: название акватории, бухты, ориентиры на берегу и т.п.
        TEXT
      end

      def ask_encounter_type
        <<~TEXT
          🔍 *Тип встречи*

          Уточните характер обнаружения:
          • *Единичная встреча* — одна или несколько особей в конкретной точке
          • *Множественная встреча* — множество особей в некотором радиусе от точки
        TEXT
      end

      def ask_encounter_radius
        <<~TEXT
          📏 *Радиус распространения*

          Укажите примерный радиус (в метрах), в пределах которого были обнаружены множественные особи.
          Пример: `50` или `100`
        TEXT
      end

      def ask_depth
        <<~TEXT
          🌊 *Глубина наблюдения*

          Укажите глубину в метрах.
          Пример: `12` или `12.5`
        TEXT
      end

      def ask_depth_precision
        <<~TEXT
          ⚙️ *Точность глубины*

          Уточните, насколько точно измерена глубина:
        TEXT
      end

      def ask_density_photos
        <<~TEXT
          📷 *Фото плотности поселения*

          Отправьте одну или несколько фотографий, демонстрирующих плотность поселения устриц.

          Когда закончите, нажмите «Готово».
        TEXT
      end

      def ask_substrate_type
        <<~TEXT
          🪨 *Тип субстрата*

          Опишите тип субстрата, на котором обнаружены устрицы (например: скальный грунт, песок, водоросли, искусственная конструкция).
        TEXT
      end

      def ask_substrate_photo
        <<~TEXT
          📷 *Фото субстрата*

          При желании отправьте фотографию субстрата или нажмите «Пропустить».
        TEXT
      end

      def ask_additional_info
        <<~TEXT
          ℹ️ *Дополнительная информация*

          Укажите любую полезную информацию: контакты для связи, погода, видимость, особенности погружения и т.п.

          Это необязательно, но желательно. Нажмите «Пропустить», если добавить нечего.
        TEXT
      end

      def ask_extra_photos
        <<~TEXT
          📷 *Дополнительные фотографии*

          Отправьте дополнительные фото с подписью (опишите фото в подписи к изображению).

          Когда закончите, нажмите «Завершить отчёт».
        TEXT
      end

      def report_saved(report_id)
        <<~TEXT
          ✅ *Отчёт успешно сохранён!*

          Номер отчёта: ##{report_id}
          Спасибо за участие в сборе данных. Учёные получат доступ к вашей записи через веб-систему.

          Чтобы отправить новый отчёт, нажмите /start
        TEXT
      end

      def invalid_date
        '❌ Неверный формат даты. Используйте формат ДД.ММ.ГГГГ (например, 15.06.2026).'
      end

      def invalid_coordinates
        '❌ Не удалось распознать координаты. Введите широту и долготу, например: 55.7558, 37.6173'
      end

      def invalid_depth
        '❌ Неверный формат глубины. Укажите число в метрах, например: 12 или 12.5'
      end

      def invalid_radius
        '❌ Неверный формат радиуса. Укажите число в метрах, например: 50'
      end

      def need_photo
        '❌ Пожалуйста, отправьте фотографию.'
      end

      def need_density_photo
        '❌ Необходимо отправить хотя бы одну фотографию плотности поселения.'
      end

      def spam_blocked
        '⛔ Слишком много сообщений. Подождите немного и попробуйте снова.'
      end

      def daily_limit_reached
        '⛔ Достигнут дневной лимит отчётов. Попробуйте завтра.'
      end

      def cancelled
        '❌ Текущий отчёт отменён. Нажмите /start, чтобы начать заново.'
      end

      def coordinates_confirmed(lat, lon)
        "✅ Координаты приняты: #{lat.round(6)}, #{lon.round(6)}. Точка отправлена на карту для проверки."
      end

      def location_confirmed
        '✅ Местоположение принято.'
      end

      def photo_added(count)
        "📷 Фото добавлено (всего: #{count}). Отправьте ещё или нажмите «Готово»."
      end

      def extra_photo_added(count)
        "📷 Дополнительное фото добавлено (всего: #{count}). Отправьте ещё или нажмите «Завершить отчёт»."
      end
    end
  end
end
