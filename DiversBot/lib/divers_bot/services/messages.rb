# frozen_string_literal: true

module DiversBot
  module Services
    module Messages
      module_function

      def welcome
        <<~TEXT
          🦪 Добро пожаловать в бот сбора данных об устрицах!

          Этот бот предназначен для дайверов, которые обнаружили устриц во время погружений. Собранные данные и фотографии будут переданы учёным для исследования.

          📋 *Что потребуется указать:*
          • Дата наблюдения
          • Описание места и координаты (геопозиция или вручную, можно пропустить координаты)
          • Тип встречи (единичная или множественная)
          • Глубина наблюдения
          • Описание и фото плотности поселения
          • Тип субстрата (с возможным фото)
          • Дополнительная информация (необязательно)
          • Дополнительные фото с описанием (необязательно)

          Нажмите «Начать отчёт», чтобы приступить к заполнению.
        TEXT
      end

      def ask_date
        <<~TEXT
          📅 *Дата наблюдения*

          Укажите дату, когда были обнаружены устрицы.
          Формат: `ДД.ММ.ГГГГ` (например, 15.06.2026)
        TEXT
      end

      def ask_location_description
        <<~TEXT
          📝 *Описание места*

          Опишите место обнаружения словами: название акватории, бухты, ориентиры на берегу и т.п.

          Это обязательный шаг. После описания можно указать координаты или пропустить их.
        TEXT
      end

      def ask_location_choice
        <<~TEXT
          📍 *Координаты места*

          Как указать точное положение?
          • *Геопозиция* — отправить текущую точку с устройства
          • *Координаты* — ввести широту и долготу вручную
          • *Пропустить координаты* — оставить только текстовое описание
        TEXT
      end

      def ask_geolocation
        <<~TEXT
          📍 *Текущая геопозиция*

          Нажмите «Отправить геопозицию» — MAX передаст координаты напрямую.
        TEXT
      end

      def ask_coordinates
        <<~TEXT
          🌐 *Координаты*

          Введите широту и долготу через запятую или пробел.
          Пример: `55.7558, 37.6173` или `55.7558 37.6173`

          После ввода бот покажет точку на карте для проверки.
        TEXT
      end

      def ask_encounter_type
        <<~TEXT
          🔍 *Тип встречи*

          Уточните характер обнаружения:
          • *Единичная встреча* — одна или несколько особей в конкретной точке
          • *Множественная встреча* — множество особей в некотором радиусе от точки
        TEXT
      end

      def ask_encounter_radius
        <<~TEXT
          📏 *Радиус распространения*

          Укажите примерный радиус (в метрах), в пределах которого были обнаружены множественные особи.
          Пример: `50` или `100`
        TEXT
      end

      def ask_depth
        <<~TEXT
          🌊 *Глубина наблюдения*

          Укажите глубину в метрах.
          Пример: `12` или `12.5`
        TEXT
      end

      def ask_depth_precision
        <<~TEXT
          ⚙️ *Точность глубины*

          Уточните, насколько точно измерена глубина:
        TEXT
      end

      def ask_density_description
        <<~TEXT
          📊 *Описание плотности поселения*

          Опишите словами плотность поселения устриц: сколько особей видно, на какой площади, как они распределены.

          Пример: «единичные особи на 1–2 м²», «плотное скопление на площади около 0.5 м²».
        TEXT
      end

      def ask_density_photos
        <<~TEXT
          📷 *Фото плотности поселения*

          При желании отправьте до 3 фотографий плотности поселения.

          Фото необязательны. Когда закончите, нажмите «Готово».
        TEXT
      end

      def ask_substrate_type
        <<~TEXT
          🪨 *Тип субстрата*

          Выберите тип субстрата, на котором обнаружены устрицы:

          • *Скала* — цельная твёрдая поверхность (не рассыпается, нет стыков)
          • *Валуны* — крупные камни > 30 см в диаметре, видны границы между ними
          • *Галька*
          • *Песок*
          • *Искусственная конструкция* — бетон, затонувшее судно и т.д.

          ⚠️ Если субстрат смешанный (например, песок с камнями), выберите тот, который *преобладает*.

          Если не уверены в типе субстрата, сделайте дополнительные фото — специалисты определят по изображениям.
        TEXT
      end

      def ask_substrate_photo
        <<~TEXT
          📷 *Фото субстрата*

          При желании отправьте одну фотографию субстрата или нажмите «Пропустить».
        TEXT
      end

      def ask_additional_info
        <<~TEXT
          ℹ️ *Дополнительная информация*

          Укажите любую полезную информацию: контакты для связи, погода, видимость, особенности погружения и т.п.

          Это необязательно, но желательно. Нажмите «Пропустить», если добавить нечего.
        TEXT
      end

      def ask_extra_photos
        <<~TEXT
          📷 *Дополнительные фотографии*

          Отправьте до 5 дополнительных фото с подписью (опишите фото в подписи к изображению).

          Когда закончите, нажмите «Завершить отчёт».
        TEXT
      end

      def invalid_date
        '❌ Неверный формат даты. Используйте формат ДД.ММ.ГГГГ (например, 15.06.2026).'
      end

      def invalid_coordinates
        '❌ Не удалось распознать координаты. Введите широту и долготу, например: 55.7558, 37.6173'
      end

      def invalid_depth
        '❌ Неверный формат глубины. Укажите число в метрах, например: 12 или 12.5'
      end

      def invalid_radius
        '❌ Неверный формат радиуса. Укажите число в метрах, например: 50'
      end

      def need_photo
        '❌ Пожалуйста, отправьте фотографию.'
      end

      def spam_blocked
        '⛔ Слишком много сообщений. Подождите немного и попробуйте снова.'
      end

      def daily_limit_reached
        '⛔ Достигнут дневной лимит отчётов. Попробуйте завтра.'
      end

      def exited
        '🚪 Вы вышли из отчёта. Нажмите /start, чтобы начать заново.'
      end

      def report_saved_footer
        <<~TEXT
          Спасибо за участие в сборе данных!

          Нажмите «Начать отчёт», чтобы заполнить новый.
        TEXT
      end

      def coordinates_confirmed(lat, lon)
        "✅ Координаты приняты: #{lat.round(6)}, #{lon.round(6)}. Точка отправлена на карту для проверки."
      end

      def location_confirmed
        '✅ Местоположение принято.'
      end

      def location_description_saved
        '✅ Описание места сохранено.'
      end

      def photo_added(count, limit)
        "📷 Фото добавлено (всего: #{count} из #{limit}). Отправьте ещё или нажмите «Готово»."
      end

      def extra_photo_added(count, limit)
        "📷 Дополнительное фото добавлено (всего: #{count} из #{limit}). Отправьте ещё или нажмите «Завершить отчёт»."
      end

      def photo_limit_reached(limit, done_label: 'Готово')
        "❌ Достигнут лимит: не более #{limit} фото. Нажмите «#{done_label}», чтобы продолжить."
      end
    end
  end
end
